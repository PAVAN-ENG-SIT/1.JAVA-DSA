/* unary operators( increment operators )
 *      ++  ( )
 *      --  ( )
 * 
 */

public class threeunary {
    public static void main(String[] args){
        // int x = 10 ;
        int positivex= +10 ;
        int negativex= -10 ;

        System.out.println(--negativex);
        System.out.println(--positivex);
    }
    
}
